# What does the ** operator do in Python, and how is it used?
# ** is used to evaluate the power of something,for eg:

number = 5
square_of_number = 5 ** 2
cube_of_number = 5 ** 3
_4_times = 5 ** 4
_5_times = 5 ** 5
_6_times = 5 ** 6

print("The square of", 5, "is: ", square_of_number)
print("The cube of", 5, "is: ", cube_of_number)
print("The four times of", 5, "is: ", _4_times)
print("The five times of", 5, "is: ", _5_times)
print("The six times of", 5, "is: ", _6_times)
