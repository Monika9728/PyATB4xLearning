# POPPING THE ELEMENT
# will remove first,second,third element...... from right side from the list
cubes = [1, 8, 27, 64, 125, 216, 289, 512]
print("Popping out the 1st element in the stack:", cubes.pop())
print("Popping out the 2st element in the stack:", cubes.pop())
print("New elements in the stack:", cubes)
