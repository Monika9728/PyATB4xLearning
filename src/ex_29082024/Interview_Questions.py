squares = [1, 2, 9, 16, 25]
squares.sort(reverse=False) # MEANS THERE SHOULD BE NO REVERSE
print(squares)
print("--------------------------------------------------------------------------------------------")
squares.sort(reverse=True) #MEANS THERE SHOULD BE A REVERSE OF ELEMENTS
print(squares)
print("--------------------------------------------------------------------------------------------")
squares.reverse() #this is also same
print(squares)
print("--------------------------------------------------------------------------------------------")

